import type { AgentKnowledgeSource, InternalAgentConfig } from '../models/internal-agent-config.model';

function tools(
	rows: [
		toolId: string,
		name: string,
		description: string,
		enabled: boolean,
		requiresApproval: boolean,
		risk: 'low' | 'medium' | 'high' | 'critical',
	][],
) {
	return rows.map(([toolId, name, description, enabled, requiresApproval, riskLevel]) => ({
		toolId,
		name,
		description,
		enabled,
		requiresApproval,
		riskLevel,
	}));
}

function ks(
	rows: [id: string, name: string, type: AgentKnowledgeSource['type'], description: string, enabled: boolean][],
): AgentKnowledgeSource[] {
	return rows.map(([id, name, type, description, enabled]) => ({ id, name, type, description, enabled }));
}

