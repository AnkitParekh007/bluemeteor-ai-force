import type { Agent, AgentCategory, AgentTier, Capability } from '../models/agent.models';

type AgentExtras = Partial<
	Pick<Agent, 'longDescription' | 'relatedKeywords' | 'skillDomains' | 'tier'>
>;

function agent(
	slug: string,
	name: string,
	roleTitle: string,
	industryLabel: string,
	category: AgentCategory,
	categoryLabel: string,
	shortDescription: string,
	heroTagline: string,
	capabilities: [string, string][],
	tools: string[],
	useCases: string[],
	whyMatters: string,
	extras?: AgentExtras,
): Agent {
	const caps: Capability[] = capabilities.map(([title, description]) => ({ title, description }));
	const longDescription =
		extras?.longDescription ??
		`${shortDescription} ${heroTagline} Built for enterprise workflows: clear ownership, governance-friendly outputs, and measurable delivery — so teams scale specialized work without losing control.`;
	const skillDomains = extras?.skillDomains ?? [
		industryLabel,
		`${categoryLabel} organizations`,
		'Cross-functional delivery',
	];
	const relatedKeywords = extras?.relatedKeywords ?? [
		name,
		roleTitle,
		categoryLabel,
		industryLabel,
		...tools.slice(0, Math.min(4, tools.length)),
	];

	// Tier assignment: free-tier slugs are fronto and producto; all others default to pro
	const FREE_SLUGS = new Set(['fronto', 'producto']);
	const tier: AgentTier = extras?.tier ?? (FREE_SLUGS.has(slug) ? 'free' : 'pro');

	return {
		slug,
		name,
		roleTitle,
		category,
		categoryLabel,
		tier,
		shortDescription,
		heroTagline,
		longDescription,
		capabilities: caps,
		tools,
		skillDomains,
		useCases,
		whyMatters,
		relatedKeywords,
	};
}

